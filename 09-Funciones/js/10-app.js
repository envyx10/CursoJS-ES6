/* 

ARROW 
FUNCTIONS

*/

// Otra forma de declarar funciones, es nuevo y revolucionario de ES6
// Su sintaxis es mas corta que las tradicionales 


// Sin Arrow Functions
const aprendiendo1 = function(name){
console.log(`Aprendiendo JS`);
};

aprendiendo1();

// Con Arrow Functions
const aprendiendo2 = () => {
console.log(`Aprendiendo JS`);
}

aprendiendo2();

// Con Arrow Functions con 1 sola linea tambien se pueden resumir asi : 
const aprendiendo3 = () => `Aprendiendo JS` ;

console.log(aprendiendo3());




